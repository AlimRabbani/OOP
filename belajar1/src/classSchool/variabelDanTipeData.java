package classSchool;

public class variabelDanTipeData {

    public static void main(String[] args) {
        //membuat variabel data
        String nama, alamat;
        int usia;
        double tinggi;

        //mengisi tipe data 
        nama = "alim rabbani";
        alamat = "mamasa";
        usia = 19;
        tinggi = 165.7;
        // mencetak ke layar isi variabel 
        System.out.println("Nama karyawan : " + nama);
        System.out.println("alamat karyawan : " + alamat);
        System.out.println("usia karyawan : " + usia + "tahun");
        System.out.println("tinggi badan karyawan : " + tinggi + "cm"); 

    }

}
